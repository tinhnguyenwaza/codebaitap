--- INNER JOIN TWO TABLE 
--- CỨ PHÁP SELECT CỘT CẦN HIÊN THỊ 
--- TABLE 1
--- INNER JOIN TABLE ĐƯỢC LIÊN KẾT
--- ON ĐIỆU KIỂN LIÊN KẾT
--- CÂU 1
SELECT HOADON.MAHD, HOADON.NGAY, KHACHHANG.TENKH, KHACHHANG.DIACHI, KHACHHANG.DT 
FROM HOADON
INNER JOIN KHACHHANG 
ON HOADON.MAKH = KHACHHANG.MAKH;
---CÂU 2
