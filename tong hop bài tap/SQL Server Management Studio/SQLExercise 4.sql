---CÂU 5
SELECT VATTU.MAVT ,VATTU.TENVT
FROM HOADON
INNER JOIN CHITIETHOADON ON  CHITIETHOADON.MAHD = HOADON.MAHD
INNER JOIN VATTU ON VATTU.MAVT = CHITIETHOADON.MAVT
WHERE NGAY BETWEEN '2000-01-01' AND '2000-07-12';




---CÂU 6
